package DayOne;

public class CharDemo {

	public static void main(String[] args) {
		char ch = 's';
		System.out.println(ch);
		
		char ch2=65;
		System.out.println(ch2);
		
		char var1 ='\u00A7';
		System.out.println(var1);
		
		char var2 ='\u20AC';
		System.out.println(var2);
		
		int a ='A';
		System.out.println(a);
		
	}

}
